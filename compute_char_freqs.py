for name in data:
    for c1, c2, c3 in zip(START_SYM + name, name, name[1:] + END_SYM):
        if c1 not in freqs:
            freqs[c1] = {}

        next = c2 + c3

        if next not in freqs[c1]:
            freqs[next] = 1
        else:
            freqs[next] += 1

odds = {}
for char, cfreq in freqs.items():
    odds_arr = []
    tot = 0
    all_sum = sum(cfreq.values())
    for next_char, cnt in cfreq.items():
        tot += cnt
        odds_arr.append((tot/all_sum, next_char))

    odds[char] = odds_arr

